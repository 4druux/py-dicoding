def mencari_luas_persegi_panjang(panjang, lebar):
    luas_persegi_panjang = panjang * lebar
    return luas_persegi_panjang

persegi_panjang_1 = mencari_luas_persegi_panjang(10, 5)
print(persegi_panjang_1)

persegi_panjang_2 = mencari_luas_persegi_panjang(20, 10)
print(persegi_panjang_2)

